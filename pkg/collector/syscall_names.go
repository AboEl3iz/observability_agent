// SyscallName returns the human-readable name for an x86_64 syscall ID.
func SyscallName(id uint32) string {
	if name, ok := syscallNames[id]; ok {
		return name
	}
	return "sys_" + strconv.FormatUint(uint64(id), 10)
}
